function mask(id){
    var str = "+7 ";
    var inp= id;
    var old="+7 ";
    var exs = {
        '0': '',
        '1': '\\+',
        '2': '\\+7',
        '3': '\\+7 ',
        '4': '\\+7 \\d{1}',
        '5': '\\+7 \\d{2}',
        '6': '\\+7 \\d{3}',
        '7': '\\+7 \\d{3} ',
        '8': '\\+7 \\d{3} \\d{1}',
        '9': '\\+7 \\d{3} \\d{2}',
        '10': '\\+7 \\d{3} \\d{3}',
        '11': '\\+7 \\d{3} \\d{4}',
        '12': '\\+7 \\d{3} \\d{5}',
        '13': '\\+7 \\d{3} \\d{6}',
        '14': '\\+7 \\d{3} \\d{7}',
    };
    inp.onfocus = function(){
        if (inp.value==''){
            inp.value = old;
        }
    }

    inp.onkeyup = function(event){
        if (inp.value.search(exs[inp.value.length])==-1){
            inp.value=old;
        }
        if (event.keyCode!=8){
            if (inp.value.length<3){
                inp.value="+7 ";
            }
            if (inp.value.length==6){
                inp.value+=" ";
            }
            if (inp.value.length>14){
                inp.value=old;
            }
        }
        old = inp.value;
    }
};

